"""## Flow Event File


"""

event_file_path = '/content/Flow Events - 30-05-2024.txt'

# Load event data
df_events = pd.read_csv(event_file_path,skiprows=6,delimiter=';',names=['Event_Duration_Raw', 'Duration_in_Seconds', 'Event_Type', 'Sleep_Stage'])

def parse_event_duration(row_raw):
    parts = row_raw.split('-')
    start_str = parts[0].strip()
    end_str = parts[1].strip()

    # Parse start time
    start_dt = pd.to_datetime(start_str, format='%d.%m.%Y %H:%M:%S,%f')

    # for end time: if it doesn't contain a date, then it's the same day as start date
    if '.' not in end_str:
        end_str = start_dt.strftime('%d.%m.%Y ') + end_str

    end_dt = pd.to_datetime(end_str, format='%d.%m.%Y %H:%M:%S,%f')

    return start_dt, end_dt

# Apply the parsing function to create Start_Time and End_Time
df_events[['Start_Time', 'End_Time']] = df_events['Event_Duration_Raw'].apply(lambda x: pd.Series(parse_event_duration(x)))

print("df_events head:")
print(df_events.head())
print("\ndf_events info:")
df_events.info()

"""### Combining Nasal Airflow, Thoracic Movement, and SpO2 into a single DataFrame with a common time index, and preparing event annotations for overlay.
Interpolating NaN values in the 'SPO2' column for handling the difference in sampling rates.
"""

import pandas as pd

df_flow = df_flow.rename(columns={'Value': 'Flow'})
df_thorac = df_thorac.rename(columns={'Value': 'Thorac'})
df_spo2 = df_spo2.rename(columns={'Value': 'SPO2'})

# combining thorac and flow
df_respiration_signals = pd.merge(df_flow, df_thorac, left_index=True, right_index=True, how='outer')
# combining again :) upar wale and sp02
df_all_signals = pd.merge(df_respiration_signals, df_spo2, left_index=True, right_index=True, how='outer')

# linear interpolation to fill null values, to preserve overall trend and make signal smooth
df_all_signals['SPO2'] = df_all_signals['SPO2'].interpolate(method='linear')

# 7. Display the first few rows and a summary of the DataFrame's information for df_all_signals
print("df_all_signals head:")
print(df_all_signals.head())
print("\ndf_all_signals info:")
df_all_signals.info()

"""## Generate Visualizations


"""

import matplotlib.pyplot as plt
import pandas as pd

# padding before and after event (in seconds)
padding = pd.Timedelta(seconds=30)

for i, row in df_events.iterrows():

    start_time = row['Start_Time']
    end_time = row['End_Time']

    window_start = start_time - padding
    window_end = end_time + padding

    # Slice signal data for that window
    df_window = df_all_signals.loc[window_start:window_end]

    # Create figure
    fig, (ax1, ax2, ax3) = plt.subplots(3, 1, sharex=True, figsize=(15, 8))

    # Plot airflow
    ax1.plot(df_window.index, df_window['Flow'], color='blue')
    ax1.set_ylabel('Nasal Airflow')
    ax1.grid(True)

    # Plot thoracic movement
    ax2.plot(df_window.index, df_window['Thorac'], color='orange')
    ax2.set_ylabel('Thoracic Movement')
    ax2.grid(True)

    # Plot SpO2
    ax3.plot(df_window.index, df_window['SPO2'], color='green')
    ax3.set_ylabel('SpO₂')
    ax3.grid(True)

    # Overlay event
    ax1.axvspan(start_time, end_time, color='red', alpha=0.3)
    ax2.axvspan(start_time, end_time, color='red', alpha=0.3)
    ax3.axvspan(start_time, end_time, color='red', alpha=0.3)

    fig.suptitle(f"Event {i+1}: {start_time} to {end_time}", fontsize=14)
    plt.tight_layout()
    plt.show()

