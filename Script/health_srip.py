"""## Converting into pandas dataframe

For thorac, SPO2 and flow events.
Correct parsing and seting as the index for time-series alignment, handling the different sampling rates (32 Hz for respiration, 4 Hz for SpO₂).

"""

import pandas as pd

# Define file paths
flow_file_path = '/content/Flow - 30-05-2024.txt'
thorac_file_path = '/content/Thorac - 30-05-2024.txt'
spo2_file_path = '/content/SPO2 - 30-05-2024.txt'

# Function for loading and preprocessing data
def into_dataframe(file_path):
    df = pd.read_csv(file_path, skiprows=7, delimiter=';', decimal=',', names=['Timestamp', 'Value'])
    # Parsing 'Timestamp' to datetime objects
    df['Timestamp'] = pd.to_datetime(df['Timestamp'], format='%d.%m.%Y %H:%M:%S,%f')
    # Seting 'Timestamp' as DataFrame's index
    df = df.set_index('Timestamp')

    return df

# Loading file into DataFrame
df_flow = into_dataframe(flow_file_path)
df_thorac = into_dataframe(thorac_file_path)
df_spo2 = into_dataframe(spo2_file_path)

